package T1;

public class Produto {
    private String nome;
    private String descproduto;

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setDescproduto(String descproduto) {
        this.descproduto = descproduto;
    }

    public String getNome() {
        return nome;
    }

    public String getDescproduto() {
        return descproduto;
    }

    
}
